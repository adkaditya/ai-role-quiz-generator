import AIResponse from "../models/airesponse.model.js";
import * as aiService from "../services/ai.service.js";
import openAiClient from "../utils/openConfig.js";
export const generateQuiz = async (req, resp) => {
  try {
    const { topic, difficulty, numQuestions, categoryId } = req.body;
    const creatorId = req.user._id;

    if (!topic || !difficulty || !numQuestions || !categoryId) {
      return resp.status(400).json({ message: "topic, difficulty, numQuestions, and categoryId are required" });
    }

    const result = await aiService.generateQuiz(
      topic,
      difficulty,
      parseInt(numQuestions),
      categoryId,
      creatorId
    );

    return resp.status(201).json({
      status: "success",
      message: "Quiz generated by AI and saved to database successfully",
      data: result,
    });
  } catch (error) {
    console.log(error);
    return resp.status(500).json({
      message: "Something went wrong in AI quiz generation",
      error: error.message,
    });
  }
};


export const askAI=async(req,resp)=>{
// call ai

if(!req.body?.prompt){
  return resp.status(400).json({
    message:"Prompt is required"
  })
}

const {prompt}=req.body


const response=await openAiClient.responses.create({
   model: process.env.MODEL_NAME,
   input:prompt
})

console.log(response)

//save database:
await AIResponse.create({
    prompt:prompt,
    response:response.output_text,
    user:req.user._id
})

return resp.status(200).json({
  status:"success",
  message:"AI response",
  data:response.output_text
})
}